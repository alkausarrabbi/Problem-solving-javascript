// বাক্যকে সংখ্যায় (string to int) রুপান্তরিত করার একটি প্রোগ্রাম লেখো। দশমিক সংখ্যায়(string to float) রুপান্তরিত করার আরেকটি প্রোগ্রাম লেখো। 
// কোন ডাটার ডাটা টাইপ বের করার জন্য কোন ফাংশান ব্যবহার করা হয় -typeof
var number="100.1";
// string to int
console.log(typeof number);
number=parseInt(number);
console.log(number);
console.log(typeof number);

// string to float
var numb="100.1";
console.log(typeof numb);
numb=parseFloat(numb);
console.log(numb);
console.log(typeof numb);